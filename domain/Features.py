class Features:

    def __init__(self, tag_id, announcement_id):
        self.tag_id = tag_id
        self.announcement_id = announcement_id

    def get_tag_id(self):
        return self.tag_id

    def set_tag_id(self, tag_id):
        self.tag_id = tag_id

    def get_announcement_id(self):
        return self.announcement_id

    def set_announcement_id(self, announcement_id):
        self.announcement_id = announcement_id

